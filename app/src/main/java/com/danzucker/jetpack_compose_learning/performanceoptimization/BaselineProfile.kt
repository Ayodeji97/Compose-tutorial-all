/**
 * We have two type of compilation in android:
 * 1. AOT (Ahead of Time) compilation - This is when the code is compiled before it is run. This is done by the Android build tools.
 * 2. JIT (Just in Time) compilation - This is when the code is compiled while it is run. This is done by the Android runtime.
 */